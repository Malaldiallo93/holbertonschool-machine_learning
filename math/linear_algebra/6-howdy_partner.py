#!/usr/bin/env python3
"""cat_arrays function"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Args:
    - arr1 (list of ints/floats): The first array.
    - arr2 (list of ints/floats): The second array.

    Returns:
    - list: The new array resulting from the concatenation.
    """
    return arr1 + arr2
